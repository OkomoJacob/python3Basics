# / this returns the decimal e.g 20 / 3 = 6.666666666666667
# // this returns the whole number quotient e.g 20 / 3 = 6
# ** this returns the exponential e.g 20 ** 3 = 8000
# % this returns the remainder e.g 20 % 3 = 2

a = 20
b = 3
print(a / b)
print(a // b)
print(a ** b)
print(a % b)

# Argumented assignment operators
c = 5
c += 3
print(c)

# MATH FUNCTIONS
d = -2.5674
print(abs(round(d)))



